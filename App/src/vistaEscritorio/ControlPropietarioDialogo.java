    @Override
    public void mostrarVehiculos(ArrayList<Vehiculo> vehiculos) {
        lCantidadVehiculos.setText(String.valueOf(vehiculos.size()));
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Matr√≠cula");
        datos.addColumn("Modelo");
        datos.addColumn("Color");
        datos.addColumn("#Transitos");
        datos.addColumn("Monto total");

        datos.setRowCount(vehiculos.size());
        int fila = 0;
        for(Vehiculo v:vehiculos){
            datos.setValueAt(v.getMatricula(), fila, 0);
            datos.setValueAt(v.getModelo(), fila, 1);
            datos.setValueAt(v.getColor(), fila, 2);
            datos.setValueAt(v.getTransitos().size(), fila, 3);
            //HAY QUE HACER EL M√âTODO PARA EL CONTROL DE GASTO DEL VEH√çCULO. Tambi√©n hay que arreglar el costo del tr√°nsito
            datos.setValueAt(v.calcularGasto(), fila, 4);

            fila++;
        }
       
        tVehiculos.setModel(datos);
    }

    @Override
    public void mostrarBonificaciones(ArrayList<BonificacionAsignada> bonificaciones) {
        lCantidadBonificaciones.setText(String.valueOf(bonificaciones.size()));
        
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Nombre bonificaci√≥n");
        datos.addColumn("Puesto");
        datos.addColumn("Fecha asignada");

        datos.setRowCount(bonificaciones.size());
        int fila = 0;
        for(BonificacionAsignada b:bonificaciones){
            datos.setValueAt(b.getTipo().getNombre(), fila, 0);
            datos.setValueAt(b.getPuesto().getNombre(), fila, 1);
            
            //FORMATEAR LA FECHA
            Format f = new SimpleDateFormat("dd-M-yyyy");
            String strDate = f.format(b.getFechaAsignada());
            datos.setValueAt(strDate, fila, 2);
            fila++;
        }
       
        tBonificaciones.setModel(datos);
    }

    @Override
    public void mostrarTransitos(ArrayList<Transito> transitos) {
        lCantidadTransitos.setText(String.valueOf(transitos.size()));
       
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Puesto");
        datos.addColumn("Matr√≠cula");
        datos.addColumn("Tarifa");
        datos.addColumn("Tarifa (monto)");
        datos.addColumn("Bonific.");
        datos.addColumn("Bonific. (monto)");
        datos.addColumn("Monto pagado");
        datos.addColumn("Fecha");

        datos.setRowCount(transitos.size());
        int fila = 0;
        for(Transito t:transitos){
            datos.setValueAt(t.getPuesto().getNombre(), fila, 0);
            datos.setValueAt(t.getVehiculo().getMatricula(), fila, 1);
            datos.setValueAt(t.getVehiculo().getCategoria().getNombre(), fila, 2);
            
            datos.setValueAt(t.getTarifa(), fila, 3);
            if (t.getBonificacion()!=null){
                datos.setValueAt(t.getBonificacion().getTipo().getNombre(), fila, 4);
            }else{
                datos.setValueAt("", fila, 4);
            }
            
            datos.setValueAt(t.getBonificacionAplicada(), fila, 5);
            datos.setValueAt(t.getImporte(), fila, 6);
            
            //FORMATEAR LA FECHA
            Format f = new SimpleDateFormat("dd-M-yyyy hh:mm a");
            String strDate = f.format(t.getFecha());
            
            datos.setValueAt(strDate, fila, 7);
            
            fila++;
        }
       
        tTransitos.setModel(datos);
    }

    @Override
    public void mostrarRecargas(ArrayList<Recarga> recargasProp) {
        lCantidadRecargas.setText(String.valueOf(recargasProp.size()));
        
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Fecha recarga");
        datos.addColumn("Monto");
        datos.addColumn("Estado");
        datos.addColumn("Administrador");
        
        datos.setRowCount(recargasProp.size());
        int fila = 0;
        for(Recarga r:recargasProp){
            Format f = new SimpleDateFormat("dd-M-yyyy hh:mm a");
            String strDate = f.format(r.getFecha());
            datos.setValueAt(strDate, fila, 0);
            datos.setValueAt(r.getMonto(), fila, 1);
            datos.setValueAt(r.getEstado(), fila, 2);
            datos.setValueAt(r.getAdministrador(), fila, 3);
            
            fila++;
        }
       
        tRecargas.setModel(datos);
    }

    @Override
    public void mostrarNotificaciones(ArrayList<Notificacion> notificaciones) {
        lCantidadNotificaciones.setText(String.valueOf(notificaciones.size()));
        
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Fecha");
        datos.addColumn("Mensaje");

        datos.setRowCount(notificaciones.size());
        int fila = 0;
        for(Notificacion n: notificaciones){
            Format f = new SimpleDateFormat("dd-M-yyyy");
            String strDate = f.format(n.getFecha());
            
            datos.setValueAt(strDate, fila, 0);
            datos.setValueAt(n.getMensaje(), fila, 1);

            fila++;
        }
       
        tNotificaciones.setModel(datos);
    }

