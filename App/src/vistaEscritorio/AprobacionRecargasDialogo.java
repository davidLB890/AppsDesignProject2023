    @Override
    public void mostrarCargas(ArrayList<Recarga> recargas) {
        this.recargas=recargas;
        
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Fecha");
        datos.addColumn("Propietario");
        datos.addColumn("Monto");

        datos.setRowCount(recargas.size());
        int fila = 0;
        for(Recarga r:recargas){
            Format f = new SimpleDateFormat("dd-M-yyyy hh:mm a");
            String strDate = f.format(r.getFecha());
            
            datos.setValueAt(strDate, fila, 0);
            datos.setValueAt(r.getPropietario().getNombreCompleto(), fila, 1);
            datos.setValueAt(r.getMonto(), fila, 2);
            fila++;
        }
       
        tPendientes.setModel(datos);    
    }

